    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this,
                "Ingin Cetak Formulir ?",
                "Konfirmasi Cetak",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
            if (confirm == JOptionPane.YES_OPTION){
                try{
                    java.util.Date tanggal = new java.util.Date();
                    SimpleDateFormat db = new SimpleDateFormat("yyyy-MM-dd");
                    SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
                    SimpleDateFormat th = new SimpleDateFormat("yyyy");
                    SimpleDateFormat h = new SimpleDateFormat("EEEE");
                    koneksi.koneksi();
                    String sql  ="INSERT INTO r_suratmati set no_reg='"+noreg+"',date='"+db.format(tanggal)+"'";
                    koneksi.cn.executeUpdate(sql);
                    koneksi.conn.close();
                    
                    try {
                        File file = new File("src/report/surat_mati.jrxml");
                        jasperDesign = JRXmlLoader.load(file);
                        //param.clear();
                        param.put("no", noreg);
                        param.put("hari", h.format(tanggal));
                        param.put("tahun", th.format(tanggal));
                        param.put("tanggal", sdf.format(tanggal));
                        param.put("di", "Ciapus");
                        param.put("nama", nama.getText());
                        param.put("umur", umur.getText());
                        param.put("alamat", alamat.getText());
                        param.put("pelapor", pelapor.getText());
                        param.put("hub", hub.getText());
                        jasperReport = JasperCompileManager.compileReport(jasperDesign);
                        jasperPrint = JasperFillManager.fillReport(jasperReport, param, koneksi.getConnection());
                        JasperViewer.viewReport(jasperPrint, false);
                        
                    } catch (Exception e){
                        e.printStackTrace();
                        JOptionPane.showMessageDialog(this, "Cetak Gagal "+e.getMessage());
                    }
                    
                    this.dispose();
                    
                } catch (SQLException | HeadlessException e) {
                    JOptionPane.showMessageDialog(null, "Gagal");
                    System.out.println(e.getMessage());
                }
                
            }
    }//GEN-LAST:event_jButton1ActionPerformed

