    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this,
                "Ingin Cetak Formulir ?",
                "Konfirmasi Cetak",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
            if (confirm == JOptionPane.YES_OPTION){
                try{
                    autono();
                    koneksi.koneksi();
                    java.util.Date tanggal = new java.util.Date();
                    SimpleDateFormat th = new SimpleDateFormat("yyyy");
                    String sql  ="INSERT INTO r_surat_keluar set id_surat='"+ids+"',no='"+noreg+"'";
                    koneksi.cn.executeUpdate(sql);
                    koneksi.conn.close();
                    
                    try {
                        File file = new File("src/report/surat_keluar.jrxml");
                        jasperDesign = JRXmlLoader.load(file);
                        //param.clear();
                        param.put("no", noreg);
                        param.put("alamat", alamat);
                        param.put("desa", "DESA "+desa);
                        param.put("jenis", jComboBox1.getSelectedItem().toString());
                        param.put("kab", "PEMERINTAH KABUPATEN "+kab);
                        param.put("kec", "KECAMATAN "+kec);
                        param.put("tahun", th.format(tanggal));
                        param.put("kode", kode.getText());
                        jasperReport = JasperCompileManager.compileReport(jasperDesign);
                        jasperPrint = JasperFillManager.fillReport(jasperReport, param, koneksi.getConnection());
                        JasperViewer.viewReport(jasperPrint, false);
                        
                    } catch (Exception e){
                        e.printStackTrace();
                        JOptionPane.showMessageDialog(this, "Cetak Gagal "+e.getMessage());
                    }
                    
                    this.dispose();
                    
                } catch (SQLException | HeadlessException e) {
                    JOptionPane.showMessageDialog(null, "Gagal");
                    System.out.println(e.getMessage());
                }
                
            }
    }//GEN-LAST:event_jButton1ActionPerformed

