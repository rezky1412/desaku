    private void profil(){
        try {
            koneksi.koneksi();
            String sql = "SELECT * FROM profil_desa";
            ResultSet rs = koneksi.cn.executeQuery(sql);
            if (rs.next()) {
                kab = rs.getString(5);
                kec = rs.getString(6);
                desa = rs.getString(7);
                alamat = rs.getString(9);
            }
            rs.close();
            koneksi.conn.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }    
    }
    
    private void autono(){
        try {
            koneksi.koneksi();
            String sql = "SELECT * FROM r_surat_keluar ORDER BY no DESC";
            ResultSet rs = koneksi.cn.executeQuery(sql);
            if (rs.next()) {
                String no_reg = rs.getString("no").substring(0);
                String AN = "" + (Integer.parseInt(no_reg) + 1);
                String Nol = "";

                if(AN.length()==1)
                {Nol = "00";}
                else if(AN.length()==2)
                {Nol = "0";}
                else if(AN.length()==3)
                {Nol = "";}
                noreg = Nol + AN;
            } else {
                noreg = "001";
            }
            rs.close();
            koneksi.conn.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }    
    }
    
